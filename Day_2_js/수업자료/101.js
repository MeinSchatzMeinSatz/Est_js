// 변수 a, b 에 각각 숫자를 받아서 더하기 / 나누기 / 곱하기 / 빼기
let a = 10;
let b = 5;

let result = a + b;
console.log(result);

result = a - b;
console.log(result);

result = a * b;
console.log(result);

result = a / b;
console.log(result);

// 연산자 - 나머지 연산자
// 홀수 짝수를 구할떄 매우 유용
console.log(5 % 4);

// 연산자 - 거듭제곱
console.log(3 ** 3);

// a = a + 1;
a += 1;
console.log(a);

// a = a - 1;
a -= 1;
console.log(a);

